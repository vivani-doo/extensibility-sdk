/**
 * Set of context keys describing Meet user using the addon
 *
 * @export
 * @enum {number}
 */
export enum UserContextKeys {
  /**
   * User avatar url
   */
  AVATAR_URL = 'usr.avt',

  /**
   * Addon user color
   */
  COLOR = 'usr.clr',

  /**
   * User email
   */
  EMAIL = 'usr.eml',

  /**
   * The display name of the user.
   */
  FIRST_NAME = 'usr.fnm',

  /**
   * The display name of the user.
   */
  LAST_NAME = 'usr.lnm',
}
