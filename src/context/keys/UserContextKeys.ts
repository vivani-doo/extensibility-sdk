/**
 * Set of context keys describing Meet user using the addon
 *
 * @export
 * @enum {number}
 */
export enum UserContextKeys {
  /**
   * Addon user color
   */
  COLOR = 'usr.clr',

  /**
   * Addon user id
   */
  ID = 'usr.id',

  /**
   * The display name of the user.
   */
  DISPLAY_NAME = 'usr.dname',

  /**
   * The user's role
   */
  ROLE = 'usr.role',
}
