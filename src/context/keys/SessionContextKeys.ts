/**
 * Set of context keys describing Meet user using the addon
 *
 * @export
 * @enum {number}
 */
export enum SessionContextKeys {
  /**
   * Addon identifier
   */
  ADDON_ID = 'add.id',

  /**
   * Addon session id
   */
  ADDON_SESSION_ID = 'add.ses',

  /**
   * Addon user id
   */
  ADDON_USER_ID = 'add.usr',

  /**
   * Addon tenant id
   */
  ADDON_TENANT_ID = 'add.tnt',
}
