/**
 * Set of context keys describing Meet context
 *
 * @export
 * @enum {number}
 */
export enum MeetContextKeys {
  /**
   * Meet code
   */
  CODE = 'meet.cod',

  /**
   * Meet duration
   */
  DURATION = 'meet.dur',

  /**
   * Role user has in this meet
   */
  ROLE = 'meet.role',

  /**
   * Meet title
   */
  TITLE = 'meet.tit',
}
